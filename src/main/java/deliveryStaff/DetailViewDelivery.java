    private void readOrderInfo(){
    Scanner scanFile;
    Scanner scanWord;
    String[] arrayOfString = new String[8];
    int column;
    
    File file = new File("Order_List.txt"); // new file instance
        if(!file.exists()){ try {
            file.createNewFile();
        } catch (IOException ex) {
            Logger.getLogger(TrackOrder.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        
    try {
        scanFile = new Scanner(new File("Order_List.txt"));
        while (scanFile.hasNextLine()) {
        scanWord = new Scanner(scanFile.nextLine());
        column = 0;
        while (scanWord.hasNext()){
           arrayOfString[column] = scanWord.next();
           arrayOfString[column] = arrayOfString[column].replace('_',' ');
           column++;
        }
        if (arrayOfString[0].equals(orderId)){
            jlOrderID.setText(arrayOfString[0]);
            jlQuantity.setText(arrayOfString[3]);
            jlWeight.setText(arrayOfString[4]);
            jlDate.setText(arrayOfString[6]);
            
            if (arrayOfString[7].equals("Processing")){
                cbStatus.addItem("Processing");
                cbStatus.addItem("Dispached");
                cbStatus.addItem("Completed");
                
            } else if (arrayOfString[7].equals("Dispached")){
                cbStatus.addItem("Dispached");
                 cbStatus.addItem("Processing");
                cbStatus.addItem("Completed");
                
            } else if (arrayOfString[7].equals("Completed")){
                cbStatus.addItem("Completed");
                cbStatus.addItem("Processing");
                cbStatus.addItem("Dispached");
            }
            
               
        }
    }
    scanFile.close();
    } catch (FileNotFoundException e) {
    }
    
    }
    
        
    
