    private void updateStatus() throws IOException{
        File tempFile = new File("temp.txt");
        File oldFile = new File("Order_List.txt");

        FileWriter fw;
        BufferedWriter bw;
        PrintWriter pw;
      try (Scanner fileReader = new Scanner(oldFile)) {
          fw = new FileWriter(tempFile,true);
          bw = new BufferedWriter(fw);
          pw = new PrintWriter(bw);
          while(fileReader.hasNextLine()){
              String line = fileReader.nextLine();
              String[] value = line.split(" ");
              
              if(value[0].equals(orderId)){
                  pw.println(value[0]+" "+value[1]+" "+value[2]+" "+value[3]+" "+value[4]+" "+value[5]+" "+value[6]+" "+cbStatus.getSelectedItem().toString());
              }
              
              else{
                  pw.println(value[0]+" "+value[1]+" "+value[2]+" "+value[3]+" "+value[4]+" "+value[5]+" "+value[6]+" "+value[7]);
              }
          }
      }
        pw.flush();
        pw.close();
        bw.close();
        fw.close();
        oldFile.delete();
        tempFile.renameTo(oldFile);
        JOptionPane.showMessageDialog(null, "Updated Sucessfully.","INFORMATION!", JOptionPane.INFORMATION_MESSAGE);
        
    }
